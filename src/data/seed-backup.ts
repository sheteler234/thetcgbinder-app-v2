// Helper function to get products by page for binder display
export const getProductsForPage = (page: number, itemsPerPage = 9): Product[] => {
  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  return products.slice(startIndex, endIndex);
};

// Helper function to get total pages
export const getTotalPages = (itemsPerPage = 9): number => {
  return Math.ceil(products.length / itemsPerPage);
};

// Helper function to get products by category
export const getProductsByCategory = (categoryId: string): Product[] => {
  return products.filter(product => product.categoryId === categoryId);
};

// Helper function to get featured products
export const getFeaturedProducts = (): Product[] => {
  return products.filter(product => 
    ['product-1', 'product-4', 'product-pokemon-1', 'product-pokemon-5'].includes(product.id)
  );
};
